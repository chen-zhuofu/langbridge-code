"""Run langbridge-bench with the neighboring Kimi Code CLI as the coding agent.

For each task: checkout repo at base_commit, run ``kimi`` in goal (or prompt)
mode with the problem statement, capture ``git diff``, grade with hidden tests.

Prerequisites:
  - ``kimi`` on PATH (``curl -fsSL https://code.kimi.com/kimi-code/install.sh | bash``)
    or a built checkout at ``../kimi-code`` (``pnpm install && pnpm run build``).
  - ``kimi /login`` once so API credentials are configured, **or** rely on
    ``~/.langbridge`` Moonshot API key (passed via ``KIMI_MODEL_*`` env).

Examples:

  uv run python evals/langbridge-bench/run_eval_kimi_code.py --limit 1
  uv run python evals/langbridge-bench/run_eval_kimi_code.py --mode goal --workers 1
  KIMI_BIN=/path/to/kimi uv run python evals/langbridge-bench/run_eval_kimi_code.py
"""

from __future__ import annotations

import argparse
import concurrent.futures
import json
import os
import shlex
import shutil
import subprocess
import sys
import time
from pathlib import Path

from langbridge_code.settings import API_BASE_URL, DEFAULT_MODEL, GRADE_TIMEOUT_SECONDS, load_api_key
from langbridge_code.training import langbridge_bench, metrics

PROJECT_ROOT = Path(__file__).resolve().parents[2]
DEFAULT_KIMI_CODE_ROOT = PROJECT_ROOT.parent / "kimi-code"
EVAL_TYPE = "kimi-code"

# kimi goal mode: 0=complete, 3=blocked, 6=paused (see kimi-code docs)
GOAL_EXIT_COMPLETE = 0
GOAL_EXIT_BLOCKED = 3
GOAL_EXIT_PAUSED = 6


def resolve_kimi_argv(explicit: str | None = None) -> list[str]:
    """Return argv prefix to invoke kimi (e.g. ['kimi'] or ['node', '.../main.mjs'])."""
    if explicit:
        return shlex.split(explicit)
    env_bin = os.environ.get("KIMI_BIN", "").strip()
    if env_bin:
        return shlex.split(env_bin)

    on_path = shutil.which("kimi")
    if on_path:
        return [on_path]

    built = DEFAULT_KIMI_CODE_ROOT / "apps" / "kimi-code" / "dist" / "main.mjs"
    node = shutil.which("node")
    if built.is_file() and node:
        return [node, str(built)]

    raise RuntimeError(
        "kimi executable not found. Install Kimi Code CLI "
        "(https://moonshotai.github.io/kimi-code/) or set KIMI_BIN, or build "
        f"the neighbor repo: cd {DEFAULT_KIMI_CODE_ROOT} && pnpm install && pnpm run build"
    )


def build_prompt(spec: dict, mode: str) -> str:
    statement = (spec.get("problem_statement") or "").strip()
    if mode == "goal":
        return (
            "/goal Fix the following issue in this repository. "
            "Implement the required code changes (not tests). "
            "Run relevant tests to verify the fix before finishing.\n\n"
            f"{statement}"
        )
    return (
        "Fix the following issue in this repository. "
        "Implement code changes only (do not add or modify test files).\n\n"
        f"{statement}"
    )


def _patch_lines(diff: str) -> int:
    return sum(
        1
        for line in (diff or "").splitlines()
        if (line.startswith("+") or line.startswith("-")) and not line.startswith(("+++", "---"))
    )


KIMI_CONFIG_PATH = Path.home() / ".kimi-code" / "config.toml"
KIMI_PROVIDER_NAME = "langbridge"


def ensure_kimi_config(api_key: str, model: str | None = None) -> str:
    """Bootstrap ~/.kimi-code/config.toml from langbridge Moonshot credentials."""
    model_id = (model or DEFAULT_MODEL).strip()
    alias = f"{KIMI_PROVIDER_NAME}/{model_id}"
    base = API_BASE_URL or "https://api.moonshot.ai/v1"
    KIMI_CONFIG_PATH.parent.mkdir(parents=True, exist_ok=True)
    content = f"""# Auto-generated by langbridge-bench (run_eval_kimi_code.py)
default_model = "{alias}"

[providers.{KIMI_PROVIDER_NAME}]
type = "kimi"
base_url = "{base}"
api_key = "{api_key}"

[models."{alias}"]
provider = "{KIMI_PROVIDER_NAME}"
model = "{model_id}"
max_context_size = 262144
capabilities = ["image_in", "thinking"]

[thinking]
enabled = true
"""
    KIMI_CONFIG_PATH.write_text(content, encoding="utf-8")
    KIMI_CONFIG_PATH.chmod(0o600)
    return alias


def kimi_runtime_env() -> dict[str, str]:
    """Env for kimi subprocess: avoid KIMI_MODEL_* synthetic provider (can hang)."""
    env = os.environ.copy()
    env.setdefault("CI", "1")
    env.setdefault("KIMI_DISABLE_TELEMETRY", "1")
    for key in list(env):
        if key.startswith("KIMI_MODEL_"):
            env.pop(key, None)
    return env


def run_kimi(
    repo_dir: str,
    prompt: str,
    kimi_argv: list[str],
    model_alias: str | None,
    timeout: int,
    stdout_path: Path | None = None,
    stderr_path: Path | None = None,
):
    cmd = list(kimi_argv)
    if model_alias:
        cmd += ["-m", model_alias]
    cmd += ["-p", prompt]
    env = kimi_runtime_env()

    stdout_target = open(stdout_path, "w", encoding="utf-8") if stdout_path else subprocess.DEVNULL
    stderr_target = open(stderr_path, "w", encoding="utf-8") if stderr_path else subprocess.DEVNULL
    try:
        proc = subprocess.run(
            cmd,
            cwd=repo_dir,
            stdout=stdout_target,
            stderr=stderr_target,
            timeout=timeout,
            env=env,
        )
        return proc, False
    except subprocess.TimeoutExpired as expired:
        return expired, True
    finally:
        if stdout_path:
            stdout_target.close()
        if stderr_path:
            stderr_target.close()


def run_one_spec(
    spec,
    workspaces,
    grade,
    kimi_argv,
    model_alias,
    mode,
    timeout,
    artifacts_root: Path,
):
    task_id = spec["task_id"]
    artifacts_dir = artifacts_root / task_id
    artifacts_dir.mkdir(parents=True, exist_ok=True)
    started = time.time()
    error = ""
    timed_out = False
    diff = ""
    gt_pass = False
    grade_status = "not_graded"
    goal_status = "unknown"
    kimi_rc = None

    try:
        repo_dir, _py = workspaces.prepare(spec)
        workspaces.reset(repo_dir)

        prompt = build_prompt(spec, mode)
        (artifacts_dir / "prompt.txt").write_text(prompt, encoding="utf-8")

        stdout_path = artifacts_dir / "kimi_stdout.txt"
        stderr_path = artifacts_dir / "kimi_stderr.txt"
        result, timed_out = run_kimi(
            repo_dir,
            prompt,
            kimi_argv,
            model_alias,
            timeout,
            stdout_path=stdout_path,
            stderr_path=stderr_path,
        )
        stdout = stdout_path.read_text(encoding="utf-8") if stdout_path.exists() else ""
        stderr = stderr_path.read_text(encoding="utf-8") if stderr_path.exists() else ""
        kimi_rc = getattr(result, "returncode", None)

        if mode == "goal" and kimi_rc is not None:
            if kimi_rc == GOAL_EXIT_COMPLETE:
                goal_status = "complete"
            elif kimi_rc == GOAL_EXIT_BLOCKED:
                goal_status = "blocked"
            elif kimi_rc == GOAL_EXIT_PAUSED:
                goal_status = "paused"
            else:
                goal_status = f"exit_{kimi_rc}"

        diff = workspaces.capture_diff(repo_dir, test_files=spec.get("test_files"))
        graded = grade(task_id, diff)
        gt_pass = bool(graded.get("resolved"))
        grade_status = graded.get("status", "graded")
        workspaces.reset(repo_dir)
    except Exception as failure:  # noqa: BLE001
        error = str(failure)

    duration = round(time.time() - started, 1)
    summary = {
        "task_id": task_id,
        "repo": spec["repo"],
        "agent": "kimi-code",
        "mode": mode,
        "duration_s": duration,
        "gt_pass": gt_pass,
        "goal_status": goal_status,
        "kimi_exit_code": kimi_rc,
        "grade_status": grade_status,
        "diff_chars": len(diff),
        "patch_lines": _patch_lines(diff),
        "timed_out": timed_out,
        "error": error,
    }
    row = {
        "task_id": task_id,
        "gt_pass": gt_pass,
        "turns": None,
        "patch_lines": summary["patch_lines"],
        "grade_status": grade_status,
        "goal_status": goal_status,
        "kimi_exit_code": kimi_rc,
    }
    return summary, row


def main():
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--limit", type=int, default=0, help="Max tasks (0 = all).")
    parser.add_argument("--workers", type=int, default=1, help="Parallel tasks (default 1).")
    parser.add_argument(
        "--mode",
        choices=("goal", "prompt"),
        default="goal",
        help="goal: kimi /goal multi-turn; prompt: single -p turn.",
    )
    parser.add_argument("--model", default=os.environ.get("KIMI_MODEL", ""), help="kimi -m model alias.")
    parser.add_argument("--kimi-bin", default=None, help="kimi executable or 'node /path/to/main.mjs'.")
    parser.add_argument(
        "--timeout",
        type=int,
        default=int(os.environ.get("KIMI_EVAL_TIMEOUT_SECONDS", "3600")),
        help="Per-task kimi timeout in seconds.",
    )
    parser.add_argument("--grade-timeout", type=int, default=GRADE_TIMEOUT_SECONDS)
    parser.add_argument(
        "--out",
        default=str(PROJECT_ROOT / "evals" / "langbridge-bench" / "out"),
    )
    args = parser.parse_args()

    try:
        kimi_argv = resolve_kimi_argv(args.kimi_bin)
    except RuntimeError as err:
        print(err, file=sys.stderr)
        sys.exit(1)

    specs = langbridge_bench.specs()
    if not specs:
        sys.exit("No specs found under evals/langbridge-bench/specs/")
    if args.limit:
        specs = specs[: args.limit]

    out_dir = Path(args.out)
    artifacts_root = out_dir / "kimi_code_artifacts"
    out_dir.mkdir(parents=True, exist_ok=True)

    ws = langbridge_bench.Workspaces()
    grade = langbridge_bench.make_grader(ws, timeout=args.grade_timeout)

    try:
        api_key = load_api_key("moonshot")
    except Exception as err:  # noqa: BLE001
        print(f"Failed to load Moonshot API key: {err}", file=sys.stderr)
        sys.exit(1)

    model_id = (args.model or DEFAULT_MODEL).strip()
    model_alias = ensure_kimi_config(api_key, model_id)
    model_label = model_id
    print(
        f"Running {len(specs)} tasks via Kimi Code CLI "
        f"({shlex.join(kimi_argv)}, mode={args.mode}, workers={args.workers})."
    )

    summaries = []
    rows = []
    if args.workers <= 1:
        for spec in specs:
            summary, row = run_one_spec(
                spec, ws, grade, kimi_argv, model_alias, args.mode,
                args.timeout, artifacts_root,
            )
            summaries.append(summary)
            rows.append(row)
            _print_line(summary)
    else:
        # Parallel: each worker needs its own Workspaces (separate checkout cache).
        def _job(spec):
            local_ws = langbridge_bench.Workspaces()
            local_grade = langbridge_bench.make_grader(local_ws, timeout=args.grade_timeout)
            return run_one_spec(
                spec, local_ws, local_grade, kimi_argv, model_alias, args.mode,
                args.timeout, artifacts_root,
            )

        with concurrent.futures.ThreadPoolExecutor(max_workers=args.workers) as pool:
            futures = {pool.submit(_job, spec): spec for spec in specs}
            for future in concurrent.futures.as_completed(futures):
                try:
                    summary, row = future.result()
                except Exception as failure:  # noqa: BLE001
                    spec = futures[future]
                    summary = {
                        "task_id": spec["task_id"],
                        "error": str(failure),
                        "gt_pass": False,
                        "duration_s": 0,
                    }
                    row = {
                        "task_id": spec["task_id"],
                        "gt_pass": False,
                        "patch_lines": 0,
                        "grade_status": "error",
                    }
                summaries.append(summary)
                rows.append(row)
                _print_line(summary)

    run_summary = {
        "agent": "kimi-code",
        "mode": args.mode,
        "workers": args.workers,
        "model": model_label,
        "kimi_argv": kimi_argv,
        "summaries": summaries,
    }
    summary_path = out_dir / "kimi_code_run_summary.json"
    summary_path.write_text(json.dumps(run_summary, indent=2), encoding="utf-8")

    computed = metrics.compute_metrics("l4", rows)
    path = metrics.record_result(
        "l4",
        rows,
        model=f"kimi-code/{model_label}",
        dataset="langbridge-bench-kimi-code",
        notes=f"mode={args.mode}",
    )
    metrics.write_leaderboard()
    print(f"\nmetrics (l4 schema): {computed}")
    print(f"recorded: {path}")
    print(f"summary: {summary_path}")
    print(f"artifacts: {artifacts_root}")


def _print_line(summary: dict):
    extra = f" error={summary['error']}" if summary.get("error") else ""
    print(
        f"  {summary['task_id']}: gt_pass={summary.get('gt_pass')} "
        f"goal={summary.get('goal_status')} "
        f"{summary.get('duration_s', 0)}s{extra}"
    )


if __name__ == "__main__":
    main()
